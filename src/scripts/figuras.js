/**
 - Cada figura é definida em duas matrizes (vértices e faces)

 - Cada linha da matriz de vértices define as coordenadas (x, y, z)
 a partir da origem do sistema de coordenadas (canto superior esquerdo)

 - Cada linha da matriz de arestas referencia os vértices (v1, v2, ..., vn)

 - Cada linha da matriz de faces referencia as arestas (a1, a2, ..., an)

 Exemplo:
 figura = {
  vertexes: [
    [x1, y1, z1], // vertex1
    [x2, y2, z2], // vertex2
    [x3, y3, z3], // vertex3
    ...,
    [xn, yn, zn], // vertexN
  ],
  edges: [
    [1, 2], // edge1
    [1, 3], // edge2
    ...,
    [m, n], // edgeN
  ],
  faces: [
    [1, 2, 3], // face1
    [4, 7, 9], // face2
    ...,
    [x, y, z], // faceN
  ]
 }
 */

